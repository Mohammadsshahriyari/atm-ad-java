    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int userID = Integer.parseInt(jTextField1.getText());
        String UserName = jTextField2.getText();
        String FirstName = jTextField3.getText();
        String LastName = jTextField4.getText();
        String DOB = DateFormat.getDateInstance().format(jDateChooser1.getDate());
        int InitDeposit = Integer.parseInt(jTextField5.getText());
        int pinCode = Integer.parseInt(jPasswordField1.getText());
        
        try{
            Connection con = ProvideConnection.getCon();
           Statement st = con.createStatement();
           st.executeUpdate("INSERT INTO signup VALUES('"+userID+"','"+UserName+"','"+FirstName+"','"+LastName+"','"+DOB+"','"+InitDeposit+"','"+pinCode+"')");
           JOptionPane.showMessageDialog(null,"Account Successfully Created! "
                   + "Now you can login");
           setVisible(false);
           new Signup().setVisible(true);
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(null,"Some fields are missing");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "User-ID is already registered!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

